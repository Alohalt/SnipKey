    public static string Text(L10nKey key, AppLanguage language)
    {
        var strings = language == AppLanguage.English ? English : SimplifiedChinese;
        return strings.TryGetValue(key, out var value)
            ? value
            : SimplifiedChinese.TryGetValue(key, out var fallback)
                ? fallback
                : key.ToString();
    }

    public static string Format(L10nKey key, AppLanguage language, params object[] arguments)
    {
        return string.Format(language.Culture(), Text(key, language), arguments);
    }
